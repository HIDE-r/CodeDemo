#!/usr/bin/env perl

use feature 'say';
# 双引号内插数组时，是使用内置变量$"的值作为数组元素分隔符的，该变量默认值为空格
say qq(x${"}x);  # 输出： x x

my @arr = (11, 22, 33, 44);
$" = "-";
say "@arr";   # 11-22-33-44

# 当未将数组内插进双引号字符串时，默认使用内置变量$,来分隔列表各元素，该内置变量默认值为undef，因此默认情况下各元素被输出时紧密相连
say @arr;
$, = "_";   # 将其改为下划线
say @arr;  # 11_22_33_a_b
