#!/usr/bin/env perl

use v5.12;

# 某个变量前加上反斜线来获取该变量所保存实际数据的内存地址

my $x = 33;
say "x: $x, ", "addr: ", \$x;

# 为变量重新赋值，变量指向的内存地址不变
$x = 44;
say "x: $x, ", "addr: ", \$x;

# 修改变量数据，变量指向的内存地址不变
++$x;
say "x: $x, ", "addr: ", \$x;

# 但重新声明同名变量，原有变量将被掩盖，新变量指向新内存地址
my $x = 55;
say "x: $x, ", "addr: ", \$x;

